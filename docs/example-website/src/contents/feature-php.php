<h1>PHP features</h1>

<h2>No typographic alterations</h2>

All contents within PHP code tags are not altered by the typographic processor.

<?php
	print 'Example: a a a a a a a a a';
?>

However what is outside of these block will be:

<p>Example: a a a a a a a a.</p>
